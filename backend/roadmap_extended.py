WEEKLY_MILESTONES = {
    "Week 1": "Foundation & Fundamentals",
    "Week 2": "Core Concepts & Practice",
    "Week 3": "Advanced Topics & Projects",
    "Week 4": "Integration & Real-World Application"
}

def generate_roadmap(gaps):
    roadmap = []

    def build_phase(title, skills):
        phase = {
            "title": title,
            "items": []
        }

        for skill in skills:
            data = RESOURCE_DB.get(skill, {
                "resources": [
                    ("General Learning Resource", "https://www.google.com")
                ],
                "project": f"Build a real-world project using {skill}",
                "time": "2 Weeks",
                "difficulty": "Intermediate"
            })

            phase["items"].append({
                "skill": skill,
                "resources": data["resources"],
                "project": data["project"],
                "time": data["time"],
                "difficulty": data["difficulty"],
                "progress": 0,
                "status": "Not Started"
            })

        return phase

    roadmap.append(build_phase("Critical Skills", gaps["critical"]))
    roadmap.append(build_phase("Tools & Frameworks", gaps["tools"]))
    roadmap.append(build_phase("Supporting Skills", gaps["support"]))

    return roadmap

def generate_weekly_roadmap(gaps, max_weeks=4):
    """Generate a 4-week roadmap with weekly milestones"""
    all_gaps = gaps.get("critical", []) + gaps.get("tools", []) + gaps.get("support", [])
    
    weekly_roadmap = []
    skills_per_week = max(1, len(all_gaps) // max_weeks)
    
    for week in range(1, max_weeks + 1):
        week_num = f"Week {week}"
        start_idx = (week - 1) * skills_per_week
        end_idx = start_idx + skills_per_week if week < max_weeks else len(all_gaps)
        
        week_skills = all_gaps[start_idx:end_idx]
        
        week_data = {
            "week": week_num,
            "title": WEEKLY_MILESTONES.get(week_num, f"Week {week} Objectives"),
            "milestones": []
        }
        
        for skill in week_skills:
            data = RESOURCE_DB.get(skill, {
                "resources": [("General Resource", "https://www.google.com")],
                "project": f"Build with {skill}",
                "time": "1 Week",
                "difficulty": "Intermediate"
            })
            
            week_data["milestones"].append({
                "skill": skill,
                "resources": data["resources"],
                "project": data["project"],
                "difficulty": data["difficulty"],
                "status": "Not Started",
                "progress": 0
            })
        
        weekly_roadmap.append(week_data)
    
    return weekly_roadmap
